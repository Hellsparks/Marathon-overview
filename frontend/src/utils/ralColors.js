export function hexToRgb(hex) {
    if (!hex) return null;
    let cleanHex = hex.replace(/^#/, '');
    if (cleanHex.length === 3) {
        cleanHex = cleanHex.split('').map(c => c + c).join('');
    }
    const m = cleanHex.match(/^([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i);
    return m ? [parseInt(m[1], 16), parseInt(m[2], 16), parseInt(m[3], 16)] : null;
}

export function colorDistance(rgb1, rgb2) {
    if (!rgb1 || !rgb2) return Infinity;
    // Standard Euclidean distance squared for fast performance
    return Math.pow(rgb1[0] - rgb2[0], 2) +
        Math.pow(rgb1[1] - rgb2[1], 2) +
        Math.pow(rgb1[2] - rgb2[2], 2);
}

/**
 * Finds the closest RAL color for a given HEX string.
 * @param {string} hex - The target HEX color (e.g. '#FF0000' or 'FF0000')
 * @returns {object|null} - The closest { ral, hex, name, exact: boolean } or null if invalid
 */
export function findClosestRal(hex) {
    if (!hex) return null;
    let cleanHex = hex.replace(/^#/, '').toUpperCase().slice(0, 6);
    if (cleanHex.length === 3) {
        cleanHex = cleanHex.split('').map(c => c + c).join('');
    }

    // First check for exact matching HEX
    const exactMatch = RAL_COLORS.find(r => r.hex.replace(/^#/, '').toUpperCase() === cleanHex);
    if (exactMatch) {
        return { ...exactMatch, exact: true };
    }

    const rgbTarget = hexToRgb(cleanHex);
    if (!rgbTarget) return null;

    let minDistance = Infinity;
    let closestColor = null;

    for (const ralColor of RAL_COLORS) {
        const rgbCheck = hexToRgb(ralColor.hex);
        const dist = colorDistance(rgbTarget, rgbCheck);

        if (dist < minDistance) {
            minDistance = dist;
            closestColor = ralColor;
        }
    }

    if (!closestColor) return null;

    // An exact match in terms of distance would be 0, but realistically we flag non-identical hex codes as approx
    return { ...closestColor, exact: false };
}
